<template>
  <div
    :class="[
      'm-button',
      `m-button-${size}`,
      `m-button-${type}`,
      disabled ? 'm-button-disabled' : '',
    ]"
    @click="!disabled && $emit('on-click')"
    :aria-disabled="disabled"
  >
    <i class="m-button-icon" :class="icon" v-show="icon"></i>
    <span class="m-button-text">
      <slot></slot>
    </span>
  </div>
</template>

<script>
//按钮组件
export default {
  name: "Button",
  props: {
    icon: {
      type: String,
    },
    type: {
      //按钮类型
      type: String,
      default: "default",
      validator: function (value) {
        //校验按钮类型
        if (
          !["default", "danger", "info", "warning", "primary"].includes(value)
        ) {
          throw Error(
            `${value} is not include 'default', 'danger',  'info', 'warning', 'primary'`
          );
        } else {
          return true;
        }
      },
    },
    size: {
      type: String,
      default: "middle", //按钮大小
    },
    disabled: {
      type: Boolean,
      default: false,
    },
  },
};
</script>

